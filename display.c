static void draw_char(uint8_t x, uint8_t y, uint8_t scale, char c) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font5x7[c - 32];

    for (uint8_t col = 0; col < 5; col++) {
        uint8_t line = glyph[col];
        for (uint8_t row = 0; row < 7; row++) {
            if (line & (1 << row)) {
                for (uint8_t sy = 0; sy < scale; sy++) {
                    for (uint8_t sx = 0; sx < scale; sx++) {
                        ssd1306_pixel(&oled, x + col * scale + sx, y + row * scale + sy, true);
                    }
                }
            }
        }
    }
}

static void draw_str(uint8_t x, uint8_t y, uint8_t scale, const char *s) {
    uint8_t x_inicial = x;

    while (*s) {
        if (*s == '\n') {
            x = x_inicial;
            y += 8 * scale;
            s++;
            continue;
        }

        draw_char(x, y, scale, *s);
        x += 6 * scale;
        s++;
    }
}

void display_init(void) {
    i2c_init(OLED_I2C, 400000);

    gpio_set_function(OLED_SDA, GPIO_FUNC_I2C);
    gpio_set_function(OLED_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(OLED_SDA);
    gpio_pull_up(OLED_SCL);

    sleep_ms(100);

    ssd1306_init(&oled, OLED_W, OLED_H, false, OLED_ADDR, OLED_I2C);
    sleep_ms(100);

    ssd1306_config(&oled);
    ssd1306_fill(&oled, false);
    ssd1306_send_data(&oled);
}

void display_boot(void) {
    ssd1306_fill(&oled, false);
    draw_str(12, 8, 2, "MenteViva");
    draw_str(8, 34, 1, "Estimulador Cognitivo");
    draw_str(22, 50, 1, "Iniciando...");
    ssd1306_send_data(&oled);
}

void display_menu(uint8_t cursor) {
    const char *itens[] = {"Memoria", "Reflexo", "Historico"};
    char buf[24];

    ssd1306_fill(&oled, false);

    draw_str(14, 0, 1, "=== MenteViva ===");
    ssd1306_hline(&oled, 0, 127, 10, true);

    for (int i = 0; i < 3; i++) {
        snprintf(buf, sizeof(buf), "%c %s", i == cursor ? '>' : ' ', itens[i]);
        draw_str(10, 18 + i * 14, 1, buf);
    }

    draw_str(0, 56, 1, "A=ok Joy=move");
    ssd1306_send_data(&oled);
}

